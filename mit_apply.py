    o1_mini_user = "Applicant with some background, looking to do O1 tasks"

    # Suppose we have 100 applicants in a list
    applicants = [f"Applicant_{i}" for i in range(1, 101)]

    # We create a ThreadPoolExecutor with a maximum of 10 threads
    with concurrent.futures.ThreadPoolExecutor(max_workers=10) as executor:
        futures = []
        # Submit each applicant to the pool
        for applicant in applicants:
            future = executor.submit(run_apply, applicant, linkedin, o1_mini_user)
            futures.append(future)

        # Wait for all tasks to complete (or fail)
        concurrent.futures.wait(futures)

